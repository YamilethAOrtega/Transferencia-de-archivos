    private void btnEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnviarActionPerformed
        nombreArchivoe=txtArchivo.getText();
        dirIp=(txtIp.getText());
        
        try {
            Socket socket = new Socket(dirIp,puerto);
            
            PrintWriter out = new PrintWriter(socket.getOutputStream(), true);
            out.println(new File(nombreArchivoe).getName());
            
            
            FileInputStream fis = new FileInputStream(nombreArchivoe);
            BufferedInputStream bis = new BufferedInputStream(fis);
            OutputStream os = socket.getOutputStream();
            
            byte[] buffer = new byte[1024];
            int bytesRead;
            while ((bytesRead = bis.read(buffer)) != -1) {
                os.write(buffer, 0, bytesRead);
            }
            
           
            bis.close();
            socket.close();
            JOptionPane.showMessageDialog(this, "El Archivo fue enviado.");
            System.out.println("El Archivo fue enviado.");
        } catch (IOException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnEnviarActionPerformed

